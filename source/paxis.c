//	printf("HETATM  %3d  O   HOH A %3d    %8.3f%8.3f%8.3f \n",2,2,center[0]+normvec[dmin_v][0]*3.0,center[1]+normvec[dmin_v][1]*3.0,center[2]+normvec[dmin_v][2]*3.0);
