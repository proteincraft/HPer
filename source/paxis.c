void delete_pos(int pos, int bestn,float best_score[BESTPOSN],float best_score2[BESTPOSN],int best_v[BESTPOSN],int best_ijk[BESTPOSN][3], float best_other[BESTPOSN][10] )
{
	int ii,jj;

	for(ii=pos;ii<bestn-1;ii++){
		best_score[ii]=best_score[ii+1];
		best_score2[ii]=best_score2[ii+1];
		best_v[ii]=best_v[ii+1];
		for(jj=0;jj<3;jj++) {
				best_ijk[ii][jj]=best_ijk[ii+1][jj];
				best_other[ii][jj]=best_other[ii+1][jj];
		}
		for(jj=3;jj<10;jj++){
				best_other[ii][jj] = best_other[ii+1][jj];
		
		}
	}
}
#define TRANSSTEP 0.3

int save_best_to(float score, float best_score[BESTPOSN], float score2,float best_score2[BESTPOSN],int *bestn,int iv,int i,int j,int k,int best_v[BESTPOSN],int best_ijk[BESTPOSN][3],float best_other[BESTPOSN][10],float len)
{
	int iii;
	float x[3][3],px[3][3];
	int ii,jj;
	float dis2sum,dis2sum2;
	int bn=(*bestn);

	for(ii=-1;ii<=1;ii++){
		px[ii+1][0]=i*TRANSSTEP+normvec[iv][0]*len/2.0*ii;
		px[ii+1][1]=j*TRANSSTEP+normvec[iv][1]*len/2.0*ii;
		px[ii+1][2]=k*TRANSSTEP+normvec[iv][2]*len/2.0*ii;
	}

	for(iii=0;iii<bn;iii++){
		for(ii=-1;ii<=1;ii++){
			for(jj=0;jj<3;jj++){
				x[ii+1][jj]=best_ijk[iii][jj]*TRANSSTEP+normvec[best_v[iii]][jj]*len/2.0*ii;
			}
		}
		dis2sum=0.0;dis2sum2=0.0;
		for(ii=0;ii<3;ii++){
			dis2sum+=(px[ii][0]-x[ii][0])*(px[ii][0]-x[ii][0])+(px[ii][1]-x[ii][1])*(px[ii][1]-x[ii][1])+(px[ii][2]-x[ii][2])*(px[ii][2]-x[ii][2]);
		}
		for(ii=0;ii<3;ii++){
			dis2sum2+=(px[ii][0]-x[2-ii][0])*(px[ii][0]-x[2-ii][0])+(px[ii][1]-x[2-ii][1])*(px[ii][1]-x[2-ii][1])+(px[ii][2]-x[2-ii][2])*(px[ii][2]-x[2-ii][2]);
		}
		if(dis2sum<2.4*2.4*3||dis2sum2<2.4*2.4*3){
			if(score2<=best_score2[iii]) return -1;
			else{
				delete_pos(iii,bn,best_score,best_score2,best_v,best_ijk,best_other);
				bn--;
				iii--;
			}
		}
	}
	
	(*bestn)=bn;
	for(iii=0;iii<bn;iii++){
		if(score2>best_score2[iii])
			return iii;
	}

	return iii;
}
void save_best(float score, float best_score[BESTPOSN],float score2, float best_score2[BESTPOSN],int iv,int best_v[BESTPOSN],int i,int j,int k,int best_ijk[BESTPOSN][3],float best_other[BESTPOSN][10],float other_score[10],int bestn, int pos)
{
	int ii,jj,asdf;

	for(ii=bestn-1;ii>pos;ii--){
		best_score[ii]=best_score[ii-1];
		best_score2[ii]=best_score2[ii-1];
		best_v[ii]=best_v[ii-1];
		for(jj=0;jj<3;jj++)
		{
				best_ijk[ii][jj]=best_ijk[ii-1][jj];
				best_other[ii][jj]=best_other[ii-1][jj];
		}
		for(jj=3;jj<10;jj++){
				best_other[ii][jj]=best_other[ii-1][jj];
		}

	}
	best_score[pos]=score;
	best_score2[pos]=score2;
	//printf("This is best_score2[%d]:%e",pos,best_score2[pos]);
	best_v[pos]=iv;
	best_ijk[pos][0]=i;
	best_ijk[pos][1]=j;
	best_ijk[pos][2]=k;
	for (asdf = 0;asdf <10;asdf++) {best_other[pos][asdf] = other_score[asdf];}

}


